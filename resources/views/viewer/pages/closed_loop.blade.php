                                <h3>PRODUCE BLUE BOOK OVERVIEW</h3>
                                <p>In a closed-loop recycling system, packaging is designed with the
                                    goal that all of the material can be recycled into the same type
                                    of product after it reaches the end user. This yields less waste
                                    than an open-loop system where only some components of the
                                    packaging are reused and those that are used suffer degradation
                                    in the recycling process. Supply chain sustainability is the
                                    main focus of closed-loop recycling.</p>
                                <p>According to the U.S. Environmental Protection Agency (EPA),
                                    plastic recycling results in significant energy savings compared
                                    with production of new plastics using virgin material. In fact,
                                    recycling plastic takes 88 percent less energy than making
                                    plastic from raw materials.</p>
                                <p>Closed-loop systems are not new, however, they have not been as
                                    effective as open-loop recycling. This is a result of little
                                    demand for post-consumer recycling programs and a lack of
                                    consumer education about the recycling process. Historically,
                                    consumers have been taught to throw all packaging into one bin
                                    for recycling. However, different plastics have different
                                    chemical compositions and cannot be recycled together.</p>
                                <p>For example, PET or polyethylene terephthalate bottles cannot be
                                    mixed with polypropylene to create a viable product, since they
                                    have each very different melt flows. Some packaging companies
                                    are taking notice and focusing their efforts on improving the
                                    closed-loop recycling system in their packaging designs.</p>

